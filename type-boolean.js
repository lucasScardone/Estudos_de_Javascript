const primeiroNumero = 5;
const segundoNumero = 5;
const terceiroNumero = 10;
// O tipo Boolean só tem dois estados, True e False. podemos usar "===" como comparador.
console.log(primeiroNumero === segundoNumero);
console.log(primeiroNumero === terceiroNumero);

// Podemos já declarar uma variável como True ou False.
let cadastroAtivado = true;
console.log(cadastroAtivado);

// Javascript é case sensitive, ou seja, precisamos tomar cuidado com maiúsculas e minúsculas.
const texto1 = "Alura";
const texto2 = "Alura";
const texto3 = "alura";

console.log(texto1 === texto2);
console.log(texto1 === texto3);